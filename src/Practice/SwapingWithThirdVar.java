package src.Practice;

public class SwapingWithThirdVar {
    public static void main(String[] args) {
        int a= 10, b = 20;
        System.out.println("before Swaping the number :" +a+"  "+b);

        int t= a ;
        a= b;
        b= t;

        System.out.println("before Swaping the number :" +a+"  "+b);

    }
}
